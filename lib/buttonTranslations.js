/**
 * Pick a random translated version of an English button label.
 * Falls back to the English text if no translations exist.
 */
export function pickRandomTranslation(english, excludeLang = null) {
  const map = buttonTranslations[english];
  if (!map) {
    return { text: english, lang: "en" };
  }

  const codes = Object.keys(map).filter((code) => code !== excludeLang);
  if (codes.length === 0) {
    return { text: english, lang: "en" };
  }

  const lang = codes[Math.floor(Math.random() * codes.length)];
  return { text: map[lang], lang };
}
